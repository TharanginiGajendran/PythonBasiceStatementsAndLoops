
# else statement is used with if condition,
# when the if condition is not satisfied then the else statement is executed
# syntax
# if condition:
#     statement inside if
# else:
#     statement inside else
# statement outside if and else

age = 20
if age < 18:
    print("Age is less than 18 false")
else:
    print(f"Age is {age}")
print("******End********")
