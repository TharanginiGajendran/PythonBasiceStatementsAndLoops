
# if-elif-else statement is used when decision has to be among more than two alternatives

# syntax
# if condition1:
#    statement inside if
# elif condition2:
#    statement inside elif
# else:
#    statement inside else
# statement outside if,elif and else

x = 4
if x == 5:
    print("x is equal to 5")
elif x < 5:
    print("x is less than 5")
else:
    print("x is greater than 5")
print("***********End*********")